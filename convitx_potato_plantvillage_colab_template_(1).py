# Make Divisible
# ==========================================================

def _make_divisible(v, divisor=8, min_value=None):

    if min_value is None:
        min_value = divisor

    new_v = max(
        min_value,
        int(v + divisor / 2) // divisor * divisor
    )

    if new_v < 0.9 * v:
        new_v += divisor

    return new_v

# ==========================================================
# Inverted Residual Block
# ==========================================================

def _inverted_res_block(inputs,
                        expansion,
                        stride,
                        alpha,
                        filters,
                        block_id):

    channel_axis = -1

    in_channels = K.int_shape(inputs)[channel_axis]

    pointwise_conv_filters = int(filters * alpha)

    pointwise_filters = _make_divisible(
        pointwise_conv_filters,
        8
    )

    x = inputs

    prefix = f"block_{block_id}_"

    # Expansion
    x = layers.Conv2D(
        expansion * in_channels,
        kernel_size=1,
        padding="same",
        use_bias=False,
        name=prefix+"expand"
    )(x)

    x = layers.BatchNormalization(
        name=prefix+"expand_BN"
    )(x)

    x = layers.ReLU(6.)(x)

    # Depthwise
    x = layers.DepthwiseConv2D(
        kernel_size=3,
        strides=stride,
        padding="same",
        use_bias=False,
        name=prefix+"depthwise"
    )(x)

    x = layers.BatchNormalization(
        name=prefix+"depthwise_BN"
    )(x)

    x = layers.ReLU(6.)(x)

    # Projection
    x = layers.Conv2D(
        pointwise_filters,
        kernel_size=1,
        padding="same",
        use_bias=False,
        name=prefix+"project"
    )(x)

    x = layers.BatchNormalization(
        name=prefix+"project_BN"
    )(x)

    return x

