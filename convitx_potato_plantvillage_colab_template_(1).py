def se_block(input_feature, ratio=4):

    channel_axis = 1 if K.image_data_format()=="channels_first" else -1

    channel = input_feature.shape[channel_axis]

    se_feature = layers.GlobalAveragePooling2D()(input_feature)

    se_feature = layers.Reshape(

        (1,1,channel)

    )(se_feature)

    se_feature = layers.Dense(

        channel // ratio,

        activation="relu",

        kernel_initializer="he_normal",

        use_bias=True,

        bias_initializer="zeros"

    )(se_feature)

    se_feature = layers.Dense(

        channel,

        activation="sigmoid",

        kernel_initializer="he_normal",

        use_bias=True,

        bias_initializer="zeros"

    )(se_feature)

    if K.image_data_format()=="channels_first":

        se_feature = layers.Permute(

            (3,1,2)

        )(se_feature)

    se_feature = layers.multiply(

        [input_feature,se_feature]

    )

    return se_feature

